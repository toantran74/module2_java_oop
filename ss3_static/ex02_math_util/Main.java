package ss3_static.ex02_math_util;

public class Main {
    public static void main(String[] args) {
        double num1 = 3.14;
        double num2 = 13.14;

        System.out.println("Cong 2 so: " + Math.add(num1, num2));
        System.out.println("Tru 2 so: " + Math.sub(num1, num2));
        System.out.println("Nhan 2 soL : " + Math.div(num1, num2));
        System.out.println("Chia 2 s0: " + Math.mul(num1, num2));
    }
}
